	function Showmore(Id){
		size_li = $('#myList' + Id + ' li').size();
	    x= (x+5 <= size_li) ? x+5 : size_li;
	    $('#myList' + Id + ' li:lt('+x+')').show();

	    $('#loadMore' + Id).css('display','none');
	    $('#showLess' + Id).css('display','inline-block');
	    
	}

	function Showless(Id){
	    x=(x-5<0) ? 1 : x-5;
	    $('#myList' + Id + ' li').not(':lt('+x+')').hide();

		$('#loadMore' + Id).css('display','inline-block');
		$('#showLess' + Id).css('display','none');
	}
	    
	    
</script>
