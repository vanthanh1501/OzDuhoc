	    for (index = 0; index < ul_length; index++) { 
		     //$('#myList' + index + ' li:lt('+x+')').hide();
		     $('#myList' + index + ' li').not(':lt('+x+')').hide();
		}
	   	    
	});


	function Showmore(Id){
		size_li = $('#' + Id + ' li').size();
	    x= (x+5 <= size_li) ? x+5 : size_li;
	    $('#' + Id + ' li:lt('+x+')').show();
	}

	function Showless(Id){
	    x=(x-5<0) ? 1 : x-5;
	    $('#' + Id + ' li').not(':lt('+x+')').hide();
	}
</script>
